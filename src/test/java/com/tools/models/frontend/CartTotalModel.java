package com.tools.models.frontend;

public class CartTotalModel {
	
	private String total;
	private String subtotal;
	private String productDiscount;
	private String shipingCost;
	private String voucherDiscount;
	
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getSubtotal() {
		return subtotal;
	}
	public void setSubtotal(String subtotal) {
		this.subtotal = subtotal;
	}
	public String getProductDiscount() {
		return productDiscount;
	}
	public void setProductDiscount(String productDiscount) {
		this.productDiscount = productDiscount;
	}
	public String getShipingCost() {
		return shipingCost;
	}
	public void setShipingCost(String shipingCost) {
		this.shipingCost = shipingCost;
	}
	public String getVoucherDiscount() {
		return voucherDiscount;
	}
	public void setVoucherDiscount(String voucherDiscount) {
		this.voucherDiscount = voucherDiscount;
	}
	
	

}
